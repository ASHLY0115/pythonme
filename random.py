import random

# Generate a random integer
random_number = random.randint(1, 100)  # Generates a random integer between 1 and 100 (inclusive)
print(random_number)

# Generate a random floating-point number
random_float = random.random()  # Generates a random float between 0 and 1
print(random_float)

# Generate a random floating-point number within a specific range
random_range = random.uniform(2.5, 5.5)  # Generates a random float between 2.5 and 5.5
print(random_range)

